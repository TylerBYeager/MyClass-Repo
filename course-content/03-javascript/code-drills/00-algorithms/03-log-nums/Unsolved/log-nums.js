// Write code to print all numbers from 1 to `num`
// Assume `num` will be a positive number

var logNums = function(num) {
    for (i = 1; i <= num; i++) {
        console.log(i);
    };
}; // i is the beginning number. we don't need to print 0. less than or equal to number. increment;

logNums(5);

